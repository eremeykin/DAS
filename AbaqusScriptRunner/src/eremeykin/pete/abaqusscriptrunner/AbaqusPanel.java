    private void AbaqusCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbaqusCheckActionPerformed
        abaqusVersion.setLength(0);
        try {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            try {
                String abaqusPath = jTextField1.getText();
                String[] args = {"whereami"};
                VersionChecker vChecker = new VersionChecker("abaqus", args, abaqusPath, "Abaqus ");
                abaqusVersion = vChecker.check();
            } catch (IOException ex) {
                Exceptions.printStackTrace(ex);
            } catch (InterruptedException ex) {
                Exceptions.printStackTrace(ex);
            }
        } finally {
            this.setCursor(null);
            if (abaqusVersion.length() > 0) {
                jButton4.setIcon(new javax.swing.ImageIcon(getClass().getResource("/res/ok.png"))); // NOI18N
                JOptionPane.showMessageDialog(null, "Abaqus detected: " + abaqusVersion.toString(), "Abaqus detected", JOptionPane.INFORMATION_MESSAGE);
            } else {
                jButton4.setIcon(new javax.swing.ImageIcon(getClass().getResource("/res/cross-icon.png"))); // NOI18N
                JOptionPane.showMessageDialog(null, "Abaqus not found!", "Abaqus not found", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_AbaqusCheckActionPerformed

