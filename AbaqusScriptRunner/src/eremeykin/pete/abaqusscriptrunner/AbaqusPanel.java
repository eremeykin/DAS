    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String windir = System.getenv("WINDIR").substring(0, 1) + ":\\";
        JFileChooser fileChooser = new JFileChooser(windir);
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int result = fileChooser.showDialog(this, "OK");
        if (result == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            boolean containsAbaqus = false;
            if (file != null) {
                for (File f : file.listFiles()) {
                    if (f.getName().equals(abaqusBat)) {
                        containsAbaqus = true;
                        break;
                    }
                }
            }
            if (!containsAbaqus) {
                JOptionPane.showMessageDialog(this, "There is no \"" + abaqusBat + "\" in the selected directory!\nProbably it is wrong path.", "Wrong path", JOptionPane.ERROR_MESSAGE);
            }
            jTextField1.setText(file.getAbsolutePath());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

